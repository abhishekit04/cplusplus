// Program to find whether a number is positive or negative

#include<stdio.h>
#include<conio.h>

int main () {
	int number;
	printf("Enter a number :\n");
	scanf("%d", &number);
	
	if(number > 0)
	{
		printf("Number is Positive");
	}
	else
	{
		printf("Number is Negative");
	}
	
	
	return 0;
	
}
	
	
